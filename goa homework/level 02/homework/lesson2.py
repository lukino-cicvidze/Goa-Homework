# 1. სტრინგი "გამარჯობას"
greetings = "გამარჯობას"

# 2. სტრინგი "ელემენტი" ათჯერ ერთ ხაზზე
element_string = "ელემენტი" * 10  # "* 10" განმეორებით დაამატებს სტრინგს

# 3. სტრინგიდან ინტეჯერი 1000
integer_value = 500 + 500  # 500 + 500 = 1000

# 4. სტრინგი "hello world" ორი სტრინგის მეშვეობით
hello_world = "hello" + " world"  # "+" შეაერთებს სტრინგებს

# 5. ბულეანური მნიშვნელობა
boolean_value = (5 * 2) == 10  # (5 * 2) = 10, შემდეგ შედარება და შედეგი არის True

# გამოსავალი
print("სტრინგი 'გამარჯობას':", greetings)
print("სტრინგი 'ელემენტი' ათჯერ:", element_string)
print("ინტეჯერი 1000:", integer_value)
print("სტრინგი 'hello world':", hello_world)
print("ბულეანური მნიშვნელობა:", boolean_value)