# მომხმარებლისგან ორი რიცხვის მიღება
num1 = input("5") # პირველი რიცხვი, როგორც სტრიქონი
num2 = input("4 ")  # მეორე რიცხვი, როგორც სტრიქონი

# რიცხვების გაერთიანება და 1-ის დამატება
combined = num1 + num2  # გაერთიანება როგორც სტრიქონი
result = int(combined) + 1  # სტრიქონი გარდაქმნა რიცხვად და 1-ის დამატება

# შედეგის გამოყვანა
print(result)