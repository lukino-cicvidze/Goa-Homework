# მომხმარებლისგან ორი რიცხვის მიღება
num1 = float(input("შეიტანეთ პირველი რიცხვი: 10 "))  # პირველი რიცხვი
num2 = float(input("შეიტანეთ მეორე რიცხვი: 5 "))  # მეორე რიცხვი

# შედარება და უდიდესის გამოტანა
if num1 > num2:
    print(f"უდიდესი რიცხვი არის: {num1}") # თუ num1-ში და num2-ში თუ შევიტანთ პირველ რიცხვს 3 და მეორეს 8-ს უდიდესი იქნება 8
else:
    print(f"უდიდესი რიცხვი არის: {num2}")