# მომხმარებლისგან რიცხვის მიღება
n = int(input("შეიყვანეთ რიცხვი: "))

# კენტი რიცხვების ჯამის გამოთვლა
odd_sum = 0
for i in range(1, n+1):  # range იწყება 1-დან (კენტი რიცხვების გარეშე)
    if i % 2 != 0:  # თუ რიცხვი არის კენტი
        odd_sum += i

# შედეგის გამოყვანა
print("კენტი რიცხვების ჯამი არის:", odd_sum) 
